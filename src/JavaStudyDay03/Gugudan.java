package JavaStudyDay03;

public class Gugudan {
    public static void main(String[] args) {
        for(int i = 2; i < 10; i++) {
            for(int j = 1; j < 10; j++) {
                //System.out.println();
                System.out.println(i + " * " + j + " = " + (i * j));
                //System.out.printf("포맷", "변수");
            }
            System.out.println();
        }
    }
}


