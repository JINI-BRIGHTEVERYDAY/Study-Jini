package JavaStudyDay0102;

public class Study_07 {
    public static void main(String[] args) {
        // 진수
        //

        int n = 10; // 10진수
        int bn = 0B1010; // 2진수
        int on = 012; // 8진수
        int xn = 0XA; // 16진수

        System.out.println(n);
        System.out.println(bn);
        System.out.println(on);
        System.out.println(xn);
    }
}
